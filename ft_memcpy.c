#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *str_d;

	if (!(dst || src))
		return (NULL);
	str_d = dst;
	while (n)
	{
		*str_d = *(char *)src;
		n--;
		src++;
		str_d++;
	}
	return (dst);
}
